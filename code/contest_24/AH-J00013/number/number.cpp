#include <bits/stdc++.h>
using namespace std;
int main()
{
    int k;
    cin >> k;
    int a[k];
    int x, y, z;
    for (int i = 1; i = k; i++)
    {
        cin >> a[k];
        if (a[k] = 1)
            ;
        x = x + 1;
        if (a[k] = 5)
            ;
        y = y + 1;
        if (a[k] = 10)
            ;
        z = z + 1;
    };
    cout << k;
    cout << x << endl;
    cout << y << endl;
    cout << z << endl;
    return 0;
}